package gallery.model;

public class Album {
	private Integer albumid;

	private String name;

	public Integer getImageid() {
		return albumid;
	}

	public void setImageid(Integer albumid) {
		this.albumid = albumid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}